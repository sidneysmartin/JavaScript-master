"use strict";
// Operadores lógicos
// https://developer.mozilla.org/en-US/docs/Web/JavaScript/Guide/Expressions_and_Operators#logical_operators
// || -> or ou
// && -> and e
// ! not não

let n1, n2, n3, n4;
n1 = 10;
n2 = 5;
n3 = 15;
n4 = 28;

n1 > n2 && n3 > n4 ? console.log("Verdadeiro") : console.log("Falso");
n1 > n2 || n3 < n4 ? console.log("Verdadeiro") : console.log("Falso");
n1 > n2 && n3 > n4 ? console.log("Verdadeiro") : console.log("Falso");
