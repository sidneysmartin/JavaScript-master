"use strict";
// while - loops através de um bloco de código enquanto uma condição especificada é verdadeira
/*
Syntax
while (condition) {
* code
}
*/

const cars = ["BMW", "Volvo", "Audi", "Citroen"];
let j = 0;
while (cars[j]) {
  console.log(cars[j]);
  j++;
}

let i = 0;

while (i <= 10) {
  console.log(i);
  i++;
}
